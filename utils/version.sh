function version() {
    local name="Chprompt"
    local version="v0.1"
    local creator="Zeronetsec"
    local homepage="https://github.com/Zeronetsec/Chprompt"

    echo -e "${N}Name: ${GG}${name}${N}"
    echo -e "${N}Version: ${GG}${version}${N}"
    echo -e "${N}Creator: ${GG}${creator}${N}"
    echo -e "${N}Homepage: ${GG}${homepage}${N}"
}

